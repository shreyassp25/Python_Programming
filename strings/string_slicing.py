# string slicing refers to obtaining only a part of the string that we want
# syntax for string slicing-

# name=  S H R E Y A S   --> Length is 7 ( indexing starts from 0 --> length-1 )
#        0 1 2 3 4 5 6

# indexing syntax-   SL=name[ind_start:ind_end]

# start index is included but end index is not included


name='shreyas'

print(name[0:5])

print(name[0:-1])

print(name[-1:3])

print(name[-1])

print(name[-5:-1])

print(name[:4])



print(name[0:7:1])


print(name[0:7:2])

















